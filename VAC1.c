#include <stdio.h>
#include <stdlib.h>
// default datatype *
void main()
{
    printf("hello world! \n"); // string const
    // primitive datatypes
    printf("%d\n", 10);    // int const
    printf("%f\n", 3.42f); // float const
    printf("%c \n", 'A');  // char const

    int a = 10;
    printf("%d\n", a); // int const
    float pi = 3.42;
    printf("%f\n", pi); // float const
    char ch = 'A';
    printf("%c \n", ch); // char const
    int  b=10; 
    printf("%d\n",b);
}